    <div style=" width:79%; margin-left: 50px; padding-top:30px">
        <p style="font-size: 20px; text-align: justify;">Un árbol es una <strong>estructura de datos, que puede definirse de forma recursiva</strong> como una estructura vacía o un elemento (nodo) más un número finito de estructuras tipo árbol llamados “subárboles.” Si dicho número de estructuras es inferior o igual a 2, se tiene un <strong>árbol binario.</strong></p>
    </div>

    <div style=" width:79%; margin-left: 50px; padding-top:30px">
        <h3>Estructura de un arbol binario</h2>
        <img src="img/figura-arbol-binario.png" alt="" height="500" style="margin-top: 10px; margin-bottom: 20px;">    
        <p style="font-size: 20px; text-align: justify;">Para comprender mejor que es un árbol comenzaremos explicando cómo está estructurado.
            <ul style="font-size:18px;">
                <li>Nodos: Se le llama Nodo a cada elemento que contiene un Árbol.</li>
                <li>Nodo Raíz: Se refiere al primer nodo de un Árbol, Solo un nodo del Árbol puede ser la raíz.</li>
                <li>Nodo Padre: Se utiliza este término para llamar a todos aquellos nodos que tiene al menos un hijo.</li>
                <li>Nodo Hijo: Los hijos son todos aquellos nodos que tiene un padre.</li>
                <li>Nodo Hermano: Los nodos hermanos son aquellos nodos que comparte a un mismo padre en común dentro de la estructura.</li>
                <li>Nodo Hoja: Son todos aquellos nodos que no tienen hijos, los cuales siempre se encuentran en los extremos de la estructura.</li>
                <li>Nodo Rama: Estos son todos aquellos nodos que no son la raíz y que además tiene al menos un hijo.</li>
            </ul>
        </p>
    </div>

    <div style=" width:79%; margin-left: 50px;">
        <h3 style=" margin-top:45px;">Tipos de árboles binarios</h2>
        <p style="font-size: 20px; text-align: justify;">
            <ul style="font-size:18px;">
                <li><strong>Un árbol binario</strong> es un árbol con raíz en el que cada nodo tiene como máximo dos hijos.</li>
                <li><strong>Un árbol binario lleno</strong> es un árbol en el que cada nodo tiene cero o dos hijos.</li>
                <li><strong>Un árbol binario perfecto</strong> es un árbol binario lleno en el que todas las hojas (vértices con cero hijos) están a la misma profundidad (distancia desde la raíz, también llamada altura).</li>
            </ul>
        </p>
