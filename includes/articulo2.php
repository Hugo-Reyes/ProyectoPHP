    <div style=" width:79%; margin-left: 50px; padding-top:30px">
        <p style="font-size: 20px; text-align: justify;">¿Qué es una lista? Una lista es una colección de elementos organizados en secuencia que puede crecer y decrecer libremente y a cuyos elementos individuales se puede acceder, insertar y eliminar en cualquier posición. Una lista lineal se almacena en la memoria principal de una computadora en posiciones sucesivas de memoria.</p>
        <p style="font-size: 20px; text-align: justify;">Las líneas así definidas se denominan listas contiguas. Los elementos de la lista se almacenan normalmente en posiciones consecutivas de memoria, es decir, almacenamiento secuencial, donde se define la constante longitud máxima, que determina el tamaño de la mayor lista que se puede tener. Se implementan a través de arreglos, donde la inserción o eliminación de un elemento excepto en la cabecera o al final de la lista necesitará una traslación de los elementos de la lista.
</p>
    </div>

    <div style=" width:79%; margin-left: 50px; padding-top:30px">
        <h3>Operaciones en listas</h2>
        <img src="img/lista-figura.png" alt="" height="500" style="margin-top: 10px; margin-bottom: 20px;">    
        <p style="font-size: 20px; text-align: justify;">Las operaciones que se pueden realizar con listas lineales contiguas son:
            <ul style="font-size:18px;">
                <li>Insertar, eliminar o localizar un elemento</li>
                <li>Determinar el tamaño (número de elementos) de la lista</li>
                <li>Recorrer la lista para localizar un determinado elemento</li>
                <li>Clasificar los elementos de la lista en orden ascendente o descendente</li>
                <li>Unir dos o más listas en una sola</li>
                <li>Dividir una lista en varias sublistas</li>
                <li>Copiar la lista</li>
                <li>Borrar la lista</li>
            </ul>
        </p>
    </div>

    <div style=" width:79%; margin-left: 50px; padding-top:30px">
        <h3>Lista enlazada</h2>
        <img src="img/figura-lista-enlazada.png" alt="" height="500" style="margin-top: 10px; margin-bottom: 20px;">    
        <p style="font-size: 20px; text-align: justify;">Una lista enlazada o encadenada es una lista que se construye empleando apuntadores. Además, no tiene un tamaño fijo, sino que puede crecer y decrecer durante la ejecución del programa. Se constituyen como variables dinámicas. En las listas enlazadas no es necesario que los elementos en la lista sean almacenados en posiciones físicas adyacentes, ya que el apuntador indica dónde se encuentra el siguiente elemento de la lista. <br><br> Por consiguiente, la inserción y borrado no exigen desplazamiento como en el caso de las listas contiguas.Una lista enlazada sin ningún elemento se llama lista vacía. Su puntero inicial o de cabecera tiene el valor nulo (null).</p>
        <p style="font-size: 20px; text-align: justify;">Una lista enlazada se define por:
            <ul style="font-size:18px;">
                <li>El tipo de sus elementos: campo de información (datos) y campo enlace (puntero)</li>
                <li>Un puntero de cabecera que permite acceder al primer elemento de la lista</li>
                <li>Un medio para detectar el último elemento de la lista: puntero nulo (null)</li>
            </ul>
        </p>
